#include <iostream>
#include <string>
#include <vector>
#include <algorithm>
using namespace std;

int solution(vector<string> lines) {
    int answer = 0;
    vector<int> start, end;

    for (int i = 0; i < lines.size(); i++) {
        int lsize = lines[i].size();
        int hh = stoi(lines[i].substr(11, 2)) * 3600 * 1000;
        int mm = stoi(lines[i].substr(14, 2)) * 60 * 1000;
        int ss = stoi(lines[i].substr(17, 2)) * 1000;
        int sss = stoi(lines[i].substr(20, 3));
        int t = stod(lines[i].substr(24, lsize - 25)) * 1000;

        int end_time = hh + mm + ss + sss;
        end.push_back(end_time);
        start.push_back(end_time - t + 1); // 0.001s -> 1
    }

    int count = 1;
    for (int i = 0; i < lines.size() - 1; i++) {
        count = 1;

        for (int j = i + 1; j < lines.size(); j++) {
            if (start[j] < end[i] + 1000) // 1s -> 1000
                count++;
        }

        answer = max(answer, count);
    }

    return max(answer, count);
}

